function copyurl(link) {
  var inputc = document.body.appendChild(document.createElement("input"));
  inputc.value = link;
  inputc.focus();
  inputc.select();
  document.execCommand("copy");
  inputc.parentNode.removeChild(inputc);
  alert("URL Copied.");
}

